package Taller;

public class OperacionesAritmeticas {
    public static void main(String[] args) {
        //3+8*5-6/3
        System.out.println(3+8*5-6/3);
        //2.5*2*3-4/2+8
        System.out.println(2.5*2*3-4/2+8);
        //2*(6-2.5)/3
        System.out.println(2*(6-2.5)/3);
        //25>18
        System.out.println(25>18);
        //3+2.5*4/3 != 4*5.2-3.8
        System.out.println((3+2.5*4/3) != (4*5.2-3.8));
        //(8.5/3.2+6.5-2.3*5/6 >= 15.8) && (21.4<81/4+2)
        System.out.println((8.5/3.2+6.5-2.3*5/6 >= 15.8) && (21.4<81/4+2));
    }
}
